// ─────────────────────────────────────────────
// 3) レア度: rarityIndex = max(W,B,S,G) + 0.05*D2 + iconicBump
//    並べてバケット (UR9/SSR14/SR22/R22/N18)
// ─────────────────────────────────────────────
const ICONIC = {関羽:0.60,呂布:0.60}; // 正史でも最上位の武(W2位/3位)。ゲーム的にUR確定の微補正
function rarityIndex(name,d,sc){
  const g=G_SCORE[name]||0;
  return Math.max(sc.W,sc.B,sc.S,g)+0.05*d[1]+(ICONIC[name]||0);
}

// ─────────────────────────────────────────────
// 4) 武器種ごとの power 指標 → survivor ステ倍率
// ─────────────────────────────────────────────
const MELEE=['sword','spear','podao','twin','mace','axe','halberd','charge','chakram'];
function powerOf(wt,d){
  const [D1,D2,D3,D4,D5,D6,D7]=d;
  if(MELEE.includes(wt))       return 0.45*D1+0.30*D3+0.15*D2+0.10*D4;
  if(wt==='bow'||wt==='crossbow') return 0.50*D1+0.20*D2+0.15*D5+0.15*D7;
  if(wt==='summon')            return 0.35*D7+0.30*D5+0.20*D1+0.15*D6;
  if(wt==='fire'||wt==='array')return 0.45*D6+0.35*D5+0.10*D7+0.10*D4;
  if(wt==='aura')              return 0.40*D7+0.30*D6+0.20*D5+0.10*D2;
  return 0.4*D1+0.3*D3+0.3*D5;
}
function clamp(v,a,b){return Math.max(a,Math.min(b,v));}
function r2(v){return Math.round(v*100)/100;}

function deriveStat(name,wt,d,rar){
  const [D1,D2,D3,D4,D5,D6,D7]=d;
  const pwr=powerOf(wt,d);                       // 0..~10
  const kind=WTYPE[wt].kind;
  // ダメージ
  const dmg=r2((0.55+pwr/10*1.10)*RAR_DMG[rar]);
  // 範囲: 系統ごとに支配軸を変える
  let areaAxis;
  if(MELEE.includes(wt)) areaAxis=D3*0.55+D4*0.30+D2*0.15;
  else if(wt==='bow'||wt==='crossbow') areaAxis=D5*0.5+D7*0.3+D1*0.2;
  else if(wt==='summon') areaAxis=D7*0.6+D5*0.4;
  else areaAxis=D6*0.6+D5*0.4; // tactic/aura
  const area=r2(0.80+areaAxis/10*0.70);
  // クールダウン倍率(小さいほど速い): 実証D1主導の信頼性
  const reliab=(D1*0.6+D5*0.2+D7*0.2)/10;
  const cd=r2(clamp(1.25-reliab*0.62,0.58,1.25));
  // 手数(発射数/兵数): 統率・戦術。系統で上限が違う
  let amtAxis=(D7*0.6+D5*0.4)/10;
  let amount;
  if(wt==='bow') amount=1+Math.round(amtAxis*2);          // 1..3
  else if(wt==='crossbow') amount=3+Math.round(amtAxis*4);// 3..7
  else if(wt==='summon') amount=1+Math.round(amtAxis*3);  // 1..4
  else if(wt==='twin') amount=2+Math.round(amtAxis*2);
  else amount=1+Math.round(amtAxis*1);
  // ノックバック
  const knock=r2(0.6+(D3*0.6+D4*0.4)/10*1.2);
  // 会心: 恐怖度D4
  const crit=r2(clamp(0.04+D4/10*0.11,0.04,0.18));
  // 持続(zone/field/summon)
  const duration=(kind==='zone'||kind==='field')?r2(2.5+D6/10*2.5):(kind==='summon'?r2(6+D7/10*4):0);
  return {dmg,area,cd,amount,knock,crit,duration,pwr:r2(pwr)};
}

